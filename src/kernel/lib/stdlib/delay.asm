[section .lib]

; ======================================================================================================================
; ----------------------------------------------------------------------------------------------------------------------
;   导出库函数
; ----------------------------------------------------------------------------------------------------------------------
global  delay
; ======================================================================================================================


; ======================================================================================================================
; ----------------------------------------------------------------------------------------------------------------------
;   常量定义，请查看 core/constant.h
; ----------------------------------------------------------------------------------------------------------------------
DELAY_VEC       equ     0x33
; ======================================================================================================================


; ======================================================================================================================
; ----------------------------------------------------------------------------------------------------------------------
;   delay(time_t delay_ms)
; invoke milli_delay
; ----------------------------------------------------------------------------------------------------------------------
delay:
    mov eax, [esp+4]
    int DELAY_VEC
    ret
; ======================================================================================================================